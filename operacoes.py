def soma(num1, num2):
    return num1 + num2

def subtracao(num1, num2):
    return num1 - num2

def multiplicacao(num1, num2):
    return num1 * num2

def divisao(num1, num2):
    return num1 / num2

def resto(num1, num2):
    return num1 % num2

def divisao_inteira(num1, num2):
    return num1 // num2

